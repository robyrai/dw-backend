package com.cs6400.demo.model;

public class GroundhogDayReport {
  private Long year;
  private Long annualSale;
  private Long dailyAverage;
  private Long ghdTotal;

  public Long getYear() {
    return year;
  }

  public void setYear(Long year) {
    this.year = year;
  }

  public Long getAnnualSale() {
    return annualSale;
  }

  public void setAnnualSale(Long annualSale) {
    this.annualSale = annualSale;
  }

  public Long getDailyAverage() {
    return dailyAverage;
  }

  public void setDailyAverage(Long dailyAverage) {
    this.dailyAverage = dailyAverage;
  }

  public Long getGhdTotal() {
    return ghdTotal;
  }

  public void setGhdTotal(Long ghdTotal) {
    this.ghdTotal = ghdTotal;
  }
}
